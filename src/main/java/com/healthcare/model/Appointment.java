package com.healthcare.model;

public class Appointment {
    private String appointmentId; // Unique ID for the appointment
    private String patientId;      // ID of the patient
    private String doctorId;       // ID of the doctor
    private String appointmentTime; // Date and time of the appointment
    private String status;         // Status of the appointment (e.g., scheduled, completed, canceled)

    // Default constructor
    public Appointment() {}

    // Getters and Setters
    public String getAppointmentId() {
        return appointmentId;
    }

    public void setAppointmentId(String appointmentId) {
        this.appointmentId = appointmentId;
    }

    public String getPatientId() {
        return patientId;
    }

    public void setPatientId(String patientId) {
        this.patientId = patientId;
    }

    public String getDoctorId() {
        return doctorId;
    }

    public void setDoctorId(String doctorId) {
        this.doctorId = doctorId;
    }

    public String getAppointmentTime() {
        return appointmentTime;
    }

    public void setAppointmentTime(String appointmentTime) {
        this.appointmentTime = appointmentTime;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
